package ExPractice;

public class EvenOdd {

	public static void main(String[] args) {
		
		int n = 13;
		
		if (n%2==0)
		{
			System.out.println("The given number "+n+" is even number");
			
		}
		else
		{
			System.out.println("The given number "+n+" is odd number");
		}
		
	}

}
